@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    body = request.get_json(silent=True)
    if not body:
        return jsonify({"error": "Request body must be JSON."}), 400

    text = body.get("text", "").strip()
    creator_id = body.get("creator_id", "").strip()

    if not text:
        return jsonify({"error": "Field 'text' is required and must be non-empty."}), 400
    if not creator_id:
        return jsonify({"error": "Field 'creator_id' is required."}), 400
    if len(text.split()) < 40:
        return jsonify({
            "error": "Text is too short for reliable analysis. Please submit at least 40 words."
        }), 400

    content_id = str(uuid.uuid4())

    # --- Signal 1: LLM semantic assessment ---
    llm_score = get_llm_score(text)

    # --- Signal 2: Stylometric heuristics ---
    style_result = get_style_score(text)
    style_score  = style_result["style_score"]

    # --- Combined confidence score (60% LLM, 40% stylometric) ---
    combined_score = get_combined_score(llm_score, style_score)
    attribution    = _get_attribution(combined_score)
    label          = _generate_label(combined_score)

    entry = {
        "content_id":    content_id,
        "creator_id":    creator_id,
        "timestamp":     datetime.now(timezone.utc).isoformat(),
        "attribution":   attribution,
        "confidence":    combined_score,
        "llm_score":     round(llm_score, 4),
        "style_score":   style_score,
        "ttr":           style_result["ttr"],
        "sent_variance": style_result["sent_variance"],
        "punct_density": style_result["punct_density"],
        "label":         label,
        "status":        "classified",
        "appeal_reasoning":  None,
        "appeal_timestamp":  None,
    }
    write_log_entry(entry)

    return jsonify({
        "content_id":  content_id,
        "attribution": attribution,
        "confidence":  combined_score,
        "llm_score":   round(llm_score, 4),
        "style_score": style_score,
        "label":       label,
    }), 200


@app.route("/appeal", methods=["POST"])
def appeal():
    body = request.get_json(silent=True)
    if not body:
        return jsonify({"error": "Request body must be JSON."}), 400

    content_id        = body.get("content_id", "").strip()
    creator_reasoning = body.get("creator_reasoning", "").strip()

    if not content_id:
        return jsonify({"error": "Field 'content_id' is required."}), 400
    if not creator_reasoning:
        return jsonify({"error": "Field 'creator_reasoning' is required and must be non-empty."}), 400

    entry = find_log_entry(content_id)
    if entry is None:
        return jsonify({"error": f"No submission found with content_id '{content_id}'."}), 404

    if entry.get("status") == "under_review":
        return jsonify({
            "error": "An appeal has already been submitted for this content.",
            "content_id": content_id,
            "status": "under_review",
        }), 409

    # Update the existing log entry in place
    entry["status"]           = "under_review"
    entry["appeal_reasoning"] = creator_reasoning
    entry["appeal_timestamp"] = datetime.now(timezone.utc).isoformat()

    return jsonify({
        "content_id": content_id,
        "status":     "under_review",
        "message":    "Your appeal has been received and will be reviewed by a human moderator.",
    }), 200


@app.route("/log", methods=["GET"])
def log():
    # Optional ?status=under_review filter for human reviewers
    status_filter = request.args.get("status")
    entries = get_log()
    if status_filter:
        entries = [e for e in entries if e.get("status") == status_filter]
    return jsonify({"entries": entries}), 200


# ────────────────────────────────────────────────────────────────────────────
# Attribution and label helpers
# ────────────────────────────────────────────────────────────────────────────

def _get_attribution(score: float) -> str:
    if score < 0.35:
        return "likely_human"
    if score < 0.65:
        return "uncertain"
    return "likely_ai"


def _generate_label(score: float) -> str:
    attribution = _get_attribution(score)

    if attribution == "likely_ai":
        return (
            "⚠ This content was likely generated with AI assistance.\n\n"
            "Our system analysed this submission using two independent methods and found "
            "strong indicators of AI-generated text. This label does not mean the work "
            "has no value — it is provided so you can make an informed decision about "
            "what you're reading.\n\n"
            "If you are the creator and believe this is incorrect, you can submit an "
            "appeal below."
        )
    if attribution == "uncertain":
        return (
            "ℹ Attribution unclear\n\n"
            "Our system found mixed signals in this submission. We could not reliably "
            "determine whether this content was written by a human or generated with AI "
            "assistance. No strong conclusion is drawn either way.\n\n"
            "Creators can submit an appeal at any time to provide additional context."
        )
    return (
        "✓ This content appears to be human-written.\n\n"
        "Our system found no strong indicators of AI generation in this submission. "
        "This is not a guarantee — detection tools are imperfect — but the signals "
        "we measured are consistent with human authorship."
    )


