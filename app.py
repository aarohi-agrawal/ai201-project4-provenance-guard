# ────────────────────────────────────────────────────────────────────────────
# Signal 1 — LLM semantic assessment
# ────────────────────────────────────────────────────────────────────────────

def get_llm_score(text: str) -> float:
    """
    Ask Groq to estimate the probability that `text` is AI-generated.
    Returns a float in [0.0, 1.0].  Falls back to 0.5 on any error so the
    pipeline never hard-crashes because of a bad API response.
    """
    prompt = (
        "You are an AI content attribution system. Analyse the text below and "
        "estimate the probability (0.0 to 1.0) that it was generated by an AI "
        "language model rather than written by a human.\n"
        "0.0 = certainly human-written, 1.0 = certainly AI-generated.\n\n"
        "Return ONLY valid JSON in this format: {\"ai_probability\": <float>}\n"
        "Do not include any explanation.\n\n"
        f"TEXT:\n{text}"
    )

    try:
        response = groq_client.chat.completions.create(
            model="llama-3.3-70b-versatile",
            messages=[{"role": "user", "content": prompt}],
            temperature=0.0,
            max_tokens=32,
        )
        raw = response.choices[0].message.content.strip()
        data = json.loads(raw)
        score = float(data["ai_probability"])
        return max(0.0, min(1.0, score))   # clamp to [0, 1]

    except Exception as exc:
        app.logger.warning("LLM signal failed: %s — defaulting to 0.5", exc)
        return 0.5


# ────────────────────────────────────────────────────────────────────────────
# Signal 2 — Stylometric heuristics
# ────────────────────────────────────────────────────────────────────────────

def _tokenise_sentences(text: str) -> list[str]:
    """Split text into sentences on .!? boundaries."""
    sentences = re.split(r'(?<=[.!?])\s+', text.strip())
    return [s for s in sentences if s.strip()]


def _type_token_ratio(words: list[str]) -> float:
    """Vocabulary diversity over the first 200 words. Higher = more diverse."""
    sample = words[:200]
    if len(sample) == 0:
        return 0.5
    return len(set(w.lower() for w in sample)) / len(sample)


def _sentence_length_variance(sentences: list[str]) -> float:
    """Standard deviation of word counts per sentence. Higher = more varied."""
    if len(sentences) < 2:
        return 0.0
    lengths = [len(s.split()) for s in sentences]
    return statistics.stdev(lengths)


def _punctuation_density(text: str, words: list[str]) -> float:
    """Count of expressive punctuation marks divided by word count."""
    if not words:
        return 0.0
    marks = re.findall(r'[;:—–()\u2026]', text)   # excludes plain commas
    return len(marks) / len(words)


def _normalise(value: float, ai_end: float, human_end: float) -> float:
    """
    Linear normalisation so that ai_end → 1.0 and human_end → 0.0.
    Works whether ai_end < human_end (e.g. TTR: low = AI) or
    ai_end > human_end (would be reversed scale).
    Result is clamped to [0, 1].
    """
    if ai_end == human_end:
        return 0.5
    score = (value - human_end) / (ai_end - human_end)
    return max(0.0, min(1.0, score))


def get_style_score(text: str) -> dict:
    """
    Compute three stylometric sub-metrics and combine them into a single
    style_score in [0, 1] where 1 = likely AI-generated.

    Returns a dict with the combined score and the three raw sub-scores
    so they can be logged individually.
    """
    words = re.findall(r"\b\w+\b", text)
    sentences = _tokenise_sentences(text)

    ttr = _type_token_ratio(words)
    variance = _sentence_length_variance(sentences)
    punct = _punctuation_density(text, words)

    # Normalise each sub-metric: lower TTR / lower variance / lower punct → AI
    # Anchors recalibrated from observed sub-score ranges on short texts:
    #   TTR:      human writing clusters 0.82–0.88, AI similar but slightly lower
    #             → widen the band so differences register
    #   variance: short texts all fall 4–10; AI tighter (< 6), human looser (> 9)
    #   punct:    AI near 0.0, human uses occasional expressive marks (> 0.02)
    ttr_norm      = _normalise(ttr,      ai_end=0.75, human_end=0.92)
    variance_norm = _normalise(variance, ai_end=4.0,  human_end=12.0)
    punct_norm    = _normalise(punct,    ai_end=0.0,  human_end=0.04)

    combined = (ttr_norm + variance_norm + punct_norm) / 3.0

    return {
        "style_score":    round(combined, 4),
        "ttr":            round(ttr, 4),
        "sent_variance":  round(variance, 4),
        "punct_density":  round(punct, 4),
        "ttr_norm":       round(ttr_norm, 4),
        "variance_norm":  round(variance_norm, 4),
        "punct_norm":     round(punct_norm, 4),
    }


# ────────────────────────────────────────────────────────────────────────────
# Combined confidence scorer
# ────────────────────────────────────────────────────────────────────────────

def get_combined_score(llm_score: float, style_score: float) -> float:
    """
    Weighted average: LLM 65%, stylometric 35%.
    LLM gets higher weight because it captures holistic semantic patterns;
    stylometric is independent but has a known bias toward short casual writing.
    """
    return round((0.65 * llm_score) + (0.35 * style_score), 4)


# ────────────────────────────────────────────────────────────────────────────
# Audit log helpers
# ────────────────────────────────────────────────────────────────────────────

def write_log_entry(entry: dict) -> None:
    _audit_log.append(entry)


def get_log(limit: int = 50) -> list[dict]:
    return _audit_log[-limit:]


def find_log_entry(content_id: str) -> dict | None:
    for entry in _audit_log:
        if entry.get("content_id") == content_id:
            return entry
    return None


